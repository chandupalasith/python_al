########### Formatting Integers ############

'''syntax : print("%d"%number)'''

"if we want to round off a value (fianl result would be an interger)"
print("%d"%100.5)

"if we want to turn a positive valut to negative value (fianl result would be an interger)"
print("-%d"%3)


"if we want to keep space before the value (fianl result would be an interger)"
print("%5d"%3)
